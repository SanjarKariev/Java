package lab5.Vector2;

public class Student {
    String x;
    int y;
    String speciality;

    public Student(String x, int y){
        this.x = x;
        this.y = y;
        this.speciality = speciality;
    }
}

class rex{
    static Student[] arr = new Student[3];

    public static void main(String[] args){
        arr[0] = new Student("Rex", 18);

        for(int i = 0; i < arr.length; i++){
            System.out.println(arr[0].x = "Diacon");
            System.out.println(arr[0].y = 18);
            System.out.println(arr[0].speciality = "Programmer");
            break;
        }
    }
}
